package com.intellion.cms.domain.dto;

public class ClinicDto {
	private String clinicName;
	private String addressLine1;
	private String addressLine2;
	private String mobile;
	private String area;
	private String city;
	private String state;
	private String pincode;
	private String email;
	private String landline;
	private String website;
	public String getClinicName() {
		return clinicName;
	}
	public void setClinicName(String clinicName) {
		this.clinicName = clinicName;
	}
	public String getAddressLine1() {
		return addressLine1;
	}
	public void setAddressLine1(String addressLine1) {
		this.addressLine1 = addressLine1;
	}
	public String getAddressLine2() {
		return addressLine2;
	}
	public void setAddressLine2(String addressLine2) {
		this.addressLine2 = addressLine2;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getLandline() {
		return landline;
	}
	public void setLandline(String landline) {
		this.landline = landline;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	@Override
	public String toString() {
		return "ClinicDto [clinicName=" + clinicName + ", addressLine1=" + addressLine1 + ", addressLine2="
				+ addressLine2 + ", mobile=" + mobile + ", area=" + area + ", city=" + city + ", state=" + state
				+ ", pincode=" + pincode + ", email=" + email + ", landline=" + landline + ", website=" + website + "]";
	}

}
